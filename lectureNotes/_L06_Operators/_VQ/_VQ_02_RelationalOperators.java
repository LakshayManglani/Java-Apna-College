public class _VQ_02_RelationalOperators {
    public static void main(String[] args) {
        // Relational :- Include ==, !=, >, <, >=, <=
        int a = 4;
        int b = 5;
        System.out.println();
        System.out.println("Equal to : " + (a == b));// False
        System.out.println("Not Equal to : " + (a != b));// True
        System.out.println("Greater then : " + (a > b));// False
        System.out.println("Lesser then : " + (a < b));// True
        System.out.println("Greater then equal to  : " + (a >= b));// False
        System.out.println("Lesser then equal to : " + (a <= b));// True
        System.out.println();
    }
}
