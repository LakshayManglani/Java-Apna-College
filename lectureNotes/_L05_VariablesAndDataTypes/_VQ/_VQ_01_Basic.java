public class _VQ_01_Basic {
    public static void main(String[] args) {
        // Output in java :-
        System.out.println("\nHello World!");// ln is used for nextline
        System.out.print("Hello User");

        // Print a pyramid Patter in java :-
        System.out.println("\n****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

        // Variables in java :-
        int candy = 4;
        float ruppes = 1.5f;
        boolean purchase = true;

        System.out.println("It is " + purchase + " that I purchase " + candy + " candy, worth ruppes " + ruppes+" only.");

        /*
         * byte < short < int < long --> Store integer value. For eg :- 9
         * float < double --> Store decimal value. For eg :- 22.34
         * bollean --> Store true or false
         * char --> Store a single character. For eg :- 2,a,*
         * String -- > Store anything. For eg :- Name, Paragraph, Numbers, Special
         * Character, etc.
         */
    }
}
