public class _VQ_04_AssignmentOperators {
    public static void main(String[] args) {
        // Assignment :- =, +=, -=, *=, /=
        int a = 4;

        a = 5;
        System.out.println();
        System.out.println(a += 10);
        System.out.println(a -= 10);
        System.out.println(a *= 5);
        System.out.println(a /= 25);
        System.out.println();
    }
}
