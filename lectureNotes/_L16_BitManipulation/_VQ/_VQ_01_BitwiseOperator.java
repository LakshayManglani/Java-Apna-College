package _VQ;

public class _VQ_01_BitwiseOperator {
    public static void main(String[] args) {
        // AND --> & Operator
        System.out.println((5 & 6));// This will give 4 of decimal

        // OR --> | Operator
        System.out.println((5 | 6));// This will give 7 of decimal

        // XOR --> ^ Operator
        System.out.println((5 ^ 6));// This will give 2 of decimal

        // One's Compliment --> ~ Operator
        System.out.println(~5);// This will give -6 of decimal

        // Binary Left Shift --> << Operator
        System.out.println(5 << 2);// This will give 20 of decimal

        // Binary Right Shift --> >> Operator
        System.out.println(6 >> 1);// This will give 3 of decimal
    }
}
